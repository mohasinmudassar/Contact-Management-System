    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
//        Checking if all the fields are filled
        if(name.getText().equals("") || phoneNumber.getText().equals("") || email.getText().equals("") || 
                notes.getText().equals("") || street.getText().equals("") || city.getText().equals("") ||
                province.getText().equals("") || country.getText().equals("") || day.getText().equals("") || 
                month.getText().equals("") || year.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please fill in all the fields!");
        }
        else{
//          IF all the fields are validm then adding the data to contacts and writing it to the file
            Contact c = new Contact(name.getText(),phoneNumber.getText(),email.getText(),notes.getText(),
            new myDate(Integer.parseInt(day.getText()),month.getText(),Integer.parseInt(year.getText()))
                    ,new Address(street.getText(),city.getText(),
            province.getText(),country.getText()));
//            Adding the instanse of contact withe the to the contacts arraylist
            contacts.add(c);
//          Writing the data to the file
            this.writeData();
            JOptionPane.showMessageDialog(this,name.getText() + " Added to Contacts!");
            this.clearFields();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

