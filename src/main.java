//MEthid to read data from the file
    void readFileData(){
        try  {  
            //the file to be opened for reading
            FileInputStream fis=new FileInputStream("contacts.txt");       
            Scanner sc=new Scanner(fis);  
//            Reading the file line by line and adding it to the contacts ArrayList
            while(sc.hasNextLine()){  
                  String data = sc.nextLine();
                  String[] contactsTemp = data.split(",");
                  Contact c = new Contact(contactsTemp[0],contactsTemp[1],contactsTemp[2],contactsTemp[3],
                          new myDate(Integer.parseInt(contactsTemp[4]),
                          contactsTemp[5],Integer.parseInt(contactsTemp[6])),new Address(contactsTemp[7],contactsTemp[8],
                                  contactsTemp[9],contactsTemp[10]));
                  contacts.add(c);     
            }  
            sc.close();     
            }catch(IOException e){  
                e.printStackTrace();  
            }
    }
//    Function to write the data to the file
    void writeData(){
        try{
            FileWriter myWriter = new FileWriter("contacts.txt");
//            Writing all the data inside the Contacts Arryalist to the file line by line
            for(int i=0;i<contacts.size();i++){
                myWriter.write(contacts.get(i).getName()+"," + contacts.get(i).getHomePhone() + "," + 
                        contacts.get(i).getEmail() + "," + contacts.get(i).getNotes() + "," + 
                        contacts.get(i).getBirthday().getDay() + "," + contacts.get(i).getBirthday().getMonth() + 
                        "," + contacts.get(i).getBirthday().getYear() + "," + contacts.get(i).getHomeAddress().street 
                + "," + contacts.get(i).getHomeAddress().city + "," + contacts.get(i).getHomeAddress().province + "," +
                        contacts.get(i).getHomeAddress().country + "\n");
            }
            myWriter.close();
            //System.out.println("Successfully wrote to the file.");
        }catch (IOException e) {
            System.out.println("An error occurred.");
            e.printStackTrace();
        }
    }
